// Helpers (unchanged)
export const getFixturesByDate = (date) =>
  fixturesData.filter((fixture) => fixture.date === date);

export const getFixturesByDay = (day) =>
  fixturesData.filter((fixture) => fixture.day === day);

export const getFixturesByStatus = (status) =>
  fixturesData.filter((fixture) => fixture.status === status);

export const getUpcomingFixtures = () =>
  fixturesData.filter((fixture) => fixture.status === "Upcoming");

export const getRecentFixtures = (limit = 4) =>
  fixturesData.slice(0, limit);

export const getFixturesGroupedByDate = () => {
  const grouped = {};
  fixturesData.forEach((fixture) => {
    if (!grouped[fixture.date]) grouped[fixture.date] = [];
    grouped[fixture.date].push(fixture);
  });
  return grouped;
};
